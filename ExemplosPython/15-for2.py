# Usando um loop for com contador

#Neste exemplo, o loop "for" é usado para iterar sobre os valores de 1 a 5. 
#O "range(1, 6)" cria uma sequência de números começando em 1 e indo até, mas não incluindo, 6.
#O contador assume os valores de 1, 2, 3, 4 e 5, e a instrução "print" exibe o valor do contador em cada iteração.

for contador in range(1, 6):
    print("Contagem:", contador)