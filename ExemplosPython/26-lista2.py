numeros = [5, 2, 8, 1, 9]
print(numeros)

# Ordenando a lista em ordem crescente
numeros.sort()
print(numeros)  # Saída: [1, 2, 5, 8, 9]

# Somando todos os números da lista
soma = sum(numeros)
print(soma)  # Saída: 25

# Encontrando o valor mínimo e máximo na lista
minimo = min(numeros)
maximo = max(numeros)
print(minimo)  # Saída: 1
print(maximo)  # Saída: 9